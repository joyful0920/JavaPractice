package ch2;

public class StringEx {

	public static void main(String[] args) {

		String name = "Jo" + "y";
		String str = name + 96;
		
		System.out.println(name);
		System.out.println(str);
		
		System.out.println(2 + " ");
		System.out.println(" " + 2);
		System.out.println(2 + "");
		System.out.println("" + 2);
		System.out.println("" + "");
		System.out.println(2 + 2 + "");
		System.out.println("" + 2 + 2);
		
	}

}